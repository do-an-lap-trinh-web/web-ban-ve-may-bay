package com.edu.hcmuaf.fit.webbanvemaybay.models;

public class HangBay {
    private int id_hang_bay;
    private String ten_hang_bay;
    private String quoc_gia;

    public HangBay() {}

    public int getId_hang_bay() { return id_hang_bay; }
    public void setId_hang_bay(int id_hang_bay) { this.id_hang_bay = id_hang_bay; }
    public String getTen_hang_bay() { return ten_hang_bay; }
    public void setTen_hang_bay(String ten_hang_bay) { this.ten_hang_bay = ten_hang_bay; }
    public String getQuoc_gia() { return quoc_gia; }
    public void setQuoc_gia(String quoc_gia) { this.quoc_gia = quoc_gia; }
}
